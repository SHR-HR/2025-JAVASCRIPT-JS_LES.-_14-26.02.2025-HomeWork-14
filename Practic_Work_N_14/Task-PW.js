// Функция: Сколько времени осталось до Нового года (привязана к кнопке)
// =========================================================================================================
const displayTimeUntilNewYear = () => {
    const now = new Date();
    const newYear = new Date(now.getFullYear() + 1, 0, 1);
    const diff = newYear - now;

    const time = {
        days: Math.floor(diff / (1000 * 60 * 60 * 24)),
        hours: Math.floor((diff / (1000 * 60 * 60)) % 24),
        minutes: Math.floor((diff / (1000 * 60)) % 60),
        seconds: Math.floor((diff / 1000) % 60)
    };

    const message = `⏳ До Нового года осталось: ${time.days} дней, ${time.hours} часов, ${time.minutes} минут, ${time.seconds} секунд.`;
    
    console.log(message);
    document.getElementById("newYearResult").textContent = message;
};




const displayFormattedDate = () => {
    const date = new Date();
    const days = ["Воскресенье", "Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота"];
    const months = [
        "Января", "Февраля", "Марта", "Апреля", "Мая", "Июня",
        "Июля", "Августа", "Сентября", "Октября", "Ноября", "Декабря"
    ];

    const formattedDate = `${date.getDate()} ${months[date.getMonth()]} ${date.getFullYear()} г. ${days[date.getDay()]}.`;

    console.log(`Текущая дата: ${formattedDate}`);
    document.getElementById("dateResult").textContent = formattedDate;
};

// =========================================================================================================
// Функция: Вывод имени каждую секунду (останавливается через 10 секунд) (привязана к кнопке)
// =========================================================================================================
const startNamePrinting = () => {
    let count = 0;
    let nameOutput = document.getElementById("nameResult");
    nameOutput.textContent = ""; // Очистка перед началом

    console.log("Начинаем вывод имени каждую секунду...");
    const interval = setInterval(() => {
        const message = `ХуанКарлоз (${count + 1})`;
        console.log(message);
        nameOutput.textContent += message + "\n";
        count++;
        if (count === 10) {
            clearInterval(interval);
            console.log("Вывод имени завершен.");
            nameOutput.textContent += "Вывод имени завершен.";
        }
    }, 1000);
};

